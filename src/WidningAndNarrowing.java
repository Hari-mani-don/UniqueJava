public class WidningAndNarrowing {
    public static void main(String hari[]){
        //Widning
        int small = 10;
        double large = small;  //type conversion
        System.out.println("Widning value is "+large);

        //Narrowing
        double large1=20;
        int small1=(int)large1;  //type casting
        System.out.println("Narrowing value is"+small1);
    }


}
